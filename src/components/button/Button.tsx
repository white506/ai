//----------------------------------------------------------------------------------------------------------------------
// Imports
//----------------------------------------------------------------------------------------------------------------------

import React from 'react'

import { EComponentSize } from '../../types'

//----------------------------------------------------------------------------------------------------------------------
// Component props
//----------------------------------------------------------------------------------------------------------------------

export interface IButtonProps {
  size?: EComponentSize
}

//----------------------------------------------------------------------------------------------------------------------
// Component statics
//----------------------------------------------------------------------------------------------------------------------

export interface IButtonStatics {
  SIZE: typeof EComponentSize
}

//----------------------------------------------------------------------------------------------------------------------
// Component
//----------------------------------------------------------------------------------------------------------------------

const ButtonComponent: React.FC<IButtonProps> = (props) => {
  const {
    size = EComponentSize.MEDIUM
  } = props

  return (
    <div className="ui-container">
    <button>
      { size }
    </button>
    </div>
  )
}

//----------------------------------------------------------------------------------------------------------------------
// Export
//----------------------------------------------------------------------------------------------------------------------

// export declare const Button: React.ForwardRefExoticComponent<IButtonProps> & IButtonStatics

export const Button = {
  ...ButtonComponent,
  SIZE: typeof EComponentSize
}
